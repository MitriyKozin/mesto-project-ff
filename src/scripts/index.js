  // –í—ã–∑—ã–≤–∞–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è addCard –∫–æ—Ç–æ—Ä–æ–π –ø–µ—Ä–µ–¥–∞—é—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏—è linkValue –∏ titleValue
