  evt.preventDefault();
  //–ò–∑ —Ñ–æ—Ä–º—ã –∏–∑–≤–ª–µ–∫–∞—é—Ç—Å—è –∑–Ω–∞—á–µ–Ω–∏—è –ø–æ–ª–µ–π link –∏ place-name
