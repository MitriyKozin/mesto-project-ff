  //–í–Ω—É—Ç—Ä–∏ —Ñ—É–Ω–∫—Ü–∏–∏ addCard –ø–æ–ª—É—á–∞–µ—Ç—Å—è —ç–ª–µ–º–µ–Ω—Ç —Å id "card-template" –∏–∑ DOM –µ–≥–æ —Å–æ–¥–µ—Ä–∂–∏–º–æ–µ –∫–ª–æ–Ω–∏—Ä—É–µ—Ç—Å—è –∏ —Å–æ—Ö—Ä–∞–Ω—è–µ—Ç—Å—è –≤ –ø–µ—Ä–µ–º–µ–Ω–Ω–æ–π cardElement.
