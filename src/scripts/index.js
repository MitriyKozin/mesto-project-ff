  //‚ú¥Ô∏è –î–æ–±–∞–≤–ª—è–µ–º —Å–ª—É—à–∞—Ç–µ–ª–∏ —Å–æ–±—ã—Ç–∏–π –Ω–∞ –∫–Ω–æ–ø–∫–∏ –Ω–æ–≤—ã—Ö –∫–∞—Ä–æ—á–µ–∫ "—É–¥–∞–ª–∏—Ç—å"
  // –í—ã–±–∏—Ä–∞–µ–º –≤—Å–µ –∫–Ω–æ–ø–∫–∏ "—É–¥–∞–ª–∏—Ç—å" —Å –∫–ª–∞—Å—Å–æ–º .card__delete
  const deleteButtons = document.querySelectorAll('.card__delete-button');
  // –ü–µ—Ä–µ–±–∏—Ä–∞–µ–º –≤—Å–µ –∫–Ω–æ–ø–∫–∏ "—É–¥–∞–ª–∏—Ç—å"
